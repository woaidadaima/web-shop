<template>
  <div class="spec-preview">
    <img :src="skuImage.imgUrl " />
    <div class="event" @mousemove="handler"></div>
    <div class="big" >
      <img :src="skuImage.imgUrl" ref="bigImg"/>
    </div>
    <div class="mask" ref="mask"></div>
  </div>
</template>

<script>
  export default {
    name: "Zoom",
    data() {
      return {
        currentIndex:0,
      }
    },
    props:['skuImageList'],
    computed:{
      skuImage(){
        // 计算当前所点击的图片
        return this.skuImageList? this.skuImageList[this.currentIndex]: {}
      }
    },
    mounted(){
      // 绑定全局事件，当有人促发时执行回调修改当前索引
      this.$bus.$on('getCurrentIndex',(index)=>{
          this.currentIndex =index
      })
    },
    methods:{
      // 获取鼠标移动信息
      handler(event){
        // 接受mask和big元素
        let mask = this.$refs.mask
        let bigImg = this.$refs.bigImg
        // mask的上面和左边的定位信息
        let left = event.offsetX - mask.offsetWidth/2
        let top = event.offsetY - mask.offsetHeight/2
        // 限制mask不能超出容器边框
        if (left < 0) left = 0
        if(left > mask.offsetWidth)left = mask.offsetWidth
        if (top < 0) top = 0
        if(top > mask.offsetHeight)top = mask.offsetHeight
        //控制mask移动
        mask.style.top = top + 'px'
        mask.style.left = left +'px'
        //控制bigImg移动，移动距离为mask移动的两倍（主要看大图片和小图片的比例这里是2倍关系）
        bigImg.style.top = -2 * top + 'px'
        bigImg.style.left = -2 * left + 'px'
      }
    }
  }
</script>

